#!/usr/bin/env bash
# generate-fixtures.sh ‚Äî Regenerate pre-built OAS 3.1 fixtures
#
# Usage: ./scripts/generate-fixtures.sh
#
# Prerequisites:
#   - Rust toolchain (cargo)
#   - jq (for JSON validation)
#
# The source schema must already exist at fixtures/oas31/source/oas31-schema.json.
# This script does NOT fetch from the network.

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
SOURCE_SCHEMA="$PROJECT_ROOT/fixtures/oas31/source/oas31-schema.json"
OUTPUT_DIR="$PROJECT_ROOT/fixtures/oas31/openai-strict"
CLI="$PROJECT_ROOT/target/release/jsonschema-llm"

# ‚îÄ‚îÄ Step 1: Verify source schema exists ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

if [[ ! -f "$SOURCE_SCHEMA" ]]; then
  echo "ERROR: Source schema not found at $SOURCE_SCHEMA"
  echo "Download it from: https://spec.openapis.org/oas/3.1/schema/2022-10-07"
  exit 1
fi

echo "‚úÖ Source schema: $SOURCE_SCHEMA"

# ‚îÄ‚îÄ Step 2: Build the CLI ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

echo "üîß Building CLI (release)..."
cargo build --release -p jsonschema-llm --manifest-path "$PROJECT_ROOT/Cargo.toml" 2>&1 | tail -3

# ‚îÄ‚îÄ Step 3: Clean previous output ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

if [[ -d "$OUTPUT_DIR" ]]; then
  echo "üóëÔ∏è  Removing previous output: $OUTPUT_DIR"
  rm -rf "$OUTPUT_DIR"
fi

# ‚îÄ‚îÄ Step 4: Generate fixtures ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

echo "‚öôÔ∏è  Generating fixtures..."
STDERR_FILE=$(mktemp)
"$CLI" convert "$SOURCE_SCHEMA" --output-dir "$OUTPUT_DIR" 2>"$STDERR_FILE" || true

# Check for component errors (report but don't fail ‚Äî some recursive schemas are expected to error)
if grep -q "Component error" "$STDERR_FILE"; then
  echo ""
  echo "‚ö†Ô∏è  Component errors (expected for deeply recursive schemas):"
  grep "Component error" "$STDERR_FILE" | while read -r line; do
    echo "  $line"
  done
  echo ""
fi
rm -f "$STDERR_FILE"

# ‚îÄ‚îÄ Step 5: Validate JSON files ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

echo "üîç Validating generated JSON files..."
TOTAL_FILES=0
INVALID_FILES=0

while IFS= read -r -d '' file; do
  TOTAL_FILES=$((TOTAL_FILES + 1))
  if ! jq . "$file" > /dev/null 2>&1; then
    echo "  ‚ùå Invalid JSON: $file"
    INVALID_FILES=$((INVALID_FILES + 1))
  fi
done < <(find "$OUTPUT_DIR" -name "*.json" -print0)

if [[ $INVALID_FILES -gt 0 ]]; then
  echo "ERROR: $INVALID_FILES invalid JSON files found"
  exit 1
fi

# ‚îÄ‚îÄ Step 6: Summary ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ

COMPONENT_COUNT=$(jq '.components | length' "$OUTPUT_DIR/manifest.json")
echo ""
echo "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê"
echo "  ‚úÖ Fixture generation complete"
echo "  üì¶ Components: $COMPONENT_COUNT"
echo "  üìÑ Total files: $TOTAL_FILES"
echo "  üìÅ Output: $OUTPUT_DIR"
echo "‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê"
